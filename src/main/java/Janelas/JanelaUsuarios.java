    private void jBCadastrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarUsuarioActionPerformed

        Usuarios u = new Usuarios();
        UsuariosDAO udao = new UsuariosDAO();

        try {
            u.setNome(jTNomeUsuario.getText());
            u.setLogin(jTLogin.getText());
            u.setSenha(jTSenha.getText());
            udao.create(u);
            modelo.recarregaTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Preencha os campos corretamente!" + e);
        }
    }//GEN-LAST:event_jBCadastrarUsuarioActionPerformed

    private void jBExcluirUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirUsuarioActionPerformed
        if (jTUsuarios.getSelectedRow() != -1) {
            Usuarios u = modelo.pegaDadosLinha(jTUsuarios.getSelectedRow());
            UsuariosDAO udao = new UsuariosDAO();
            udao.delete(u);
            modelo.recarregaTabela();
        }
    }//GEN-LAST:event_jBExcluirUsuarioActionPerformed

    private void jTUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTUsuariosMouseClicked
        Usuarios u = modelo.pegaDadosLinha(jTUsuarios.getSelectedRow());
        jTNomeUsuario.setText(u.getNome());
        jTLogin.setText(u.getLogin());
        jTSenha.setText(u.getSenha());
    }//GEN-LAST:event_jTUsuariosMouseClicked

    private void jBAlterarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarUsuarioActionPerformed
        if (jTUsuarios.getSelectedRow() != -1) {
        modelo.setValueAt(jTNomeUsuario.getText(), jTUsuarios.getSelectedRow(), 0);
        modelo.setValueAt(jTLogin.getText(), jTUsuarios.getSelectedRow(), 1);
        modelo.setValueAt(jTSenha.getText(), jTUsuarios.getSelectedRow(), 2);
        
        Usuarios u = modelo.pegaDadosLinha(jTUsuarios.getSelectedRow());
        UsuariosDAO udao = new UsuariosDAO();
        udao.update(u);
        limpaCampos();
        modelo.recarregaTabela();
        }
    }//GEN-LAST:event_jBAlterarUsuarioActionPerformed
    public void limpaCampos() {
        jTNomeUsuario.setText("");
        jTLogin.setText("");
        jTLogin.setText("");

        jTNomeUsuario.requestFocus();
    }

